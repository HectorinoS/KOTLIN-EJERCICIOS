fun main() {
    val entero1: Int = 10
    val entero2: Int = 5
    val flotante1: Float = 10.5f
    val flotante2: Float = 2.5f

    println("Entero - Suma: ${entero1 + entero2}")
    println("Entero - Resta: ${entero1 - entero2}")
    println("Flotante - Multiplicación: ${flotante1 * flotante2}")
    println("Flotante - División: ${flotante1 / flotante2}")
}
